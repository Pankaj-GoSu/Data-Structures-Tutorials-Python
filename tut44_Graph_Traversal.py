# ==== DFS Algo : Depth First Search ==========

'''
--> Depth we see from top so here we visit top or first or starting node.
--> Here First visit the starting node.
 ===> How To choose starting Node :
                In graph every nodes are equal 
                So We can choose any Node as starting Node for this Traversal operation.
    --> Then visit adjacent node of starting node (choose any)
    --> After that visit adjacent node which are not visited.
    --> IF you did not get any adjacent node then backtrack got one node back and see is 
        there any unvisited node or not.

'''